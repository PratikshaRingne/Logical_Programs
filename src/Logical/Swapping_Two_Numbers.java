package Logical;

public class Swapping_Two_Numbers 
{
	public static void main(String[] args) 
	{
		//Using Third Variable
		
		int a=20;
		int b=50;
		int c;
		
		System.out.println("--Original--\n");
		
		System.out.println("a="+a+"\nb="+b);
		
		c=a; //c=20
		a=b; //a=50
		b=c; //b=20
		
		System.out.println("\n--Swapping--\n");
		
		System.out.println("a="+a+"\nb="+b);
		 
		
		
		
		
		
	}

}
