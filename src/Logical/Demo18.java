package Logical;

public class Demo18 
{
	public static void main(String[] args) 
	{
		// Print White Spaces
		
		String S1=" Hii ! I Am Pratiksha ";
		
		int count=0;
		
		for(int i=0;i<S1.length();i++)
		{
			if(S1.charAt(i)==' ')
			{
				count++;
				
			}
				
		}
		System.out.println("Count="+count);
		
		
		
		
		
	}

}
